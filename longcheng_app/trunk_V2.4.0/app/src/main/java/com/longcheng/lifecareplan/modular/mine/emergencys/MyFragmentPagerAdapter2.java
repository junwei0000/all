package com.longcheng.lifecareplan.modular.mine.emergencys;

import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

public class MyFragmentPagerAdapter2 extends FragmentPagerAdapter {

    private List<Fragment> fragmentList;
    private List<String> titles;


    public MyFragmentPagerAdapter2(FragmentManager fragmentManager, List<Fragment> fragmentList, List<String> titles) {
        super(fragmentManager);
        this.fragmentList = fragmentList;
        this.titles = titles;
    }

    @Override
    public Fragment getItem(int position) {
        return fragmentList.get(position);
    }

    @Override
    public int getCount() {
        return fragmentList.size();
    }

    @Nullable
    @Override
    public CharSequence getPageTitle(int position) {
        return titles.get(position);
    }
}
